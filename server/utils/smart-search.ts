function removeArabicArticle(word: string): string {
  if (word.startsWith('ال')) {
    return word.substring(2);
  }
  return word;
}

// Strip common Arabic possessive/pronoun suffixes to get the root word
// e.g. سلامي→سلام، سلامك→سلام، سلامه→سلام، صلواتنا→صلوات
export function stripArabicSuffixes(word: string): string[] {
  const roots: string[] = [word];
  // Order matters: longer suffixes first
  const suffixes = ['كما', 'هما', 'كم', 'هم', 'هن', 'نا', 'ني', 'تم', 'ون', 'ين', 'ات', 'ان', 'ها', 'وا', 'ه', 'ك', 'ي', 'ا'];
  for (const suffix of suffixes) {
    if (word.endsWith(suffix) && word.length - suffix.length >= 2) {
      const root = word.slice(0, word.length - suffix.length);
      if (!roots.includes(root)) roots.push(root);
      break; // strip only the longest matching suffix
    }
  }
  return roots;
}

export function expandQuery(query: string): { 
  normalizedQuery: string;
  tokens: string[];
  expandedTerms: string[];
  themes: string[];
  topics: string[];
  rootTokens: string[];
} {
  const normalizedQuery = normalizeArabicText(query);
  
  const rawTokens = normalizedQuery
    .split(/\s+/)
    .filter(token => token.length > 1 && !STOPWORDS.has(token));
  
  const tokens: string[] = [];
  const rootTokens: string[] = [];
  for (const token of rawTokens) {
    tokens.push(token);
    const withoutArticle = removeArabicArticle(token);
    if (withoutArticle !== token && withoutArticle.length > 1) {
      tokens.push(withoutArticle);
    }
    // Also strip possessive/pronoun suffixes
    const stripped = stripArabicSuffixes(withoutArticle);
    for (const root of stripped) {
      if (!tokens.includes(root) && root.length > 1) {
        tokens.push(root);
        rootTokens.push(root);
      }
    }
  }
  
  const expandedTerms = new Set<string>(tokens);
  const themes = new Set<string>();
  const topics = new Set<string>();
  
  for (const token of tokens) {
    const baseToken = removeArabicArticle(token);
    
    if (ARABIC_SYNONYMS[token]) {
      ARABIC_SYNONYMS[token].forEach(syn => expandedTerms.add(syn));
    }
    if (ARABIC_SYNONYMS[baseToken]) {
      ARABIC_SYNONYMS[baseToken].forEach(syn => expandedTerms.add(syn));
    }
    
    for (const [word, synonyms] of Object.entries(ARABIC_SYNONYMS)) {
      if (synonyms.includes(token) || synonyms.includes(baseToken)) {
        expandedTerms.add(word);
        synonyms.forEach(syn => expandedTerms.add(syn));
      }
    }
    
    for (const [theme, keywords] of Object.entries(THEME_KEYWORDS)) {
      const normalizedTheme = removeArabicArticle(normalizeArabicText(theme));
      if (keywords.includes(token) || keywords.includes(baseToken) || 
          baseToken === normalizedTheme || token === normalizedTheme) {
        themes.add(theme);
        keywords.forEach(kw => expandedTerms.add(kw));
      }
    }
    
    for (const [topicName, topicData] of Object.entries(TOPIC_SEARCH)) {
      const normalizedTopic = removeArabicArticle(normalizeArabicText(topicName));
      if (topicData.keywords.includes(token) || topicData.keywords.includes(baseToken) ||
          baseToken === normalizedTopic || token === normalizedTopic ||
          normalizedTopic.includes(baseToken) || baseToken.includes(normalizedTopic)) {
        topics.add(topicName);
        topicData.keywords.forEach(kw => expandedTerms.add(kw));
        topicData.relatedVersePatterns.forEach(pattern => expandedTerms.add(pattern));
      }
    }
  }
  
  return {
    normalizedQuery,
    tokens,
    expandedTerms: Array.from(expandedTerms),
    themes: Array.from(themes),
    topics: Array.from(topics),
    rootTokens,
  };
}

export function calculateRelevanceScore(
  verseText: string,
  originalTokens: string[],
  expandedTerms: string[],
  themes: string[],
  topics: string[] = []
): { score: number; matchType: 'exact' | 'synonym' | 'theme' | 'topic' | 'partial'; tier: number } {
  const normalizedText = normalizeArabicText(verseText);
  let score = 0;
  let matchType: 'exact' | 'synonym' | 'theme' | 'topic' | 'partial' = 'partial';
  let tier = 4;
  
  let exactMatches = 0;
  for (const token of originalTokens) {
    if (normalizedText.includes(token)) {
      exactMatches++;
      score += 100;
      matchType = 'exact';
      tier = 1;
    }
  }
  
  let topicMatches = 0;
  if (topics.length > 0) {
    for (const topic of topics) {
      const topicData = TOPIC_SEARCH[topic];
      if (topicData) {
        for (const kw of topicData.keywords) {
          if (normalizedText.includes(kw)) {
            topicMatches++;
            score += 80;
          }
        }
        for (const pattern of topicData.relatedVersePatterns) {
          if (normalizedText.includes(pattern)) {
            topicMatches++;
            score += 60;
          }
        }
        if (topicMatches > 0 && tier > 2) {
          matchType = 'topic';
          tier = 2;
        }
      }
    }
  }
  
  let themeMatches = 0;
  if (themes.length > 0) {
    for (const theme of themes) {
      const themeKeywords = THEME_KEYWORDS[theme] || [];
      for (const kw of themeKeywords) {
        if (normalizedText.includes(kw)) {
          themeMatches++;
          score += 70;
        }
      }
      if (themeMatches > 0 && tier > 2) {
        matchType = 'theme';
        tier = 2;
      }
    }
  }
  
  let synonymMatches = 0;
  for (const term of expandedTerms) {
    if (!originalTokens.includes(term) && normalizedText.includes(term)) {
      synonymMatches++;
      score += 40;
      if (tier > 3) {
        matchType = 'synonym';
        tier = 3;
      }
    }
  }
  
  if (exactMatches > 1) score += exactMatches * 50;
  if (topicMatches > 1) score += topicMatches * 30;
  if (themeMatches > 1) score += themeMatches * 20;
  
  return { score, matchType, tier };
}

export function getSearchTermsForSQL(expandedTerms: string[]): string[] {
  return expandedTerms.slice(0, 30);
}

export function getTopicsForQuery(query: string): string[] {
  const { topics } = expandQuery(query);
  return topics;
}
