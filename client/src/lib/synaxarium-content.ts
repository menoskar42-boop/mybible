export function getMonthById(id: number): SynaxariumMonth | undefined {
  return synaxariumMonths.find(m => m.id === id);
}

export function getDayEntries(monthId: number, day: number): SynaxariumEntry[] {
  const month = getMonthById(monthId);
  if (!month) return [];
  const dayData = month.days.find(d => d.day === day);
  return dayData?.entries ?? [];
}

export function getTodaySynaxarium(): {
  month: SynaxariumMonth;
  day: SynaxariumDay;
  copticDate: { year: number; month: number; day: number };
} | null {
  const coptic = getTodayCopticDate();
  const month = getMonthById(coptic.month);
  if (!month) return null;
  const day = month.days.find(d => d.day === coptic.day) ?? month.days[0];
  return { month, day, copticDate: coptic };
}

