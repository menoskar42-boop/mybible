// ─── حساب التاريخ القبطي من الميلادي ──────────────────────────────────────────
export function gregorianToCoptic(date: Date): { day: number; month: number; year: number } {
  const y = date.getFullYear();
  const m = date.getMonth() + 1;
  const d = date.getDate();

  const a = Math.floor((14 - m) / 12);
  const y2 = y + 4800 - a;
  const m2 = m + 12 * a - 3;
  const jdn =
    d +
    Math.floor((153 * m2 + 2) / 5) +
    365 * y2 +
    Math.floor(y2 / 4) -
    Math.floor(y2 / 100) +
    Math.floor(y2 / 400) -
    32045;

  const COPTIC_EPOCH = 1824665;
  const diff = jdn - COPTIC_EPOCH;
  const fullCycles = Math.floor(diff / 1461);
  const rem = diff % 1461;
  const extraYears = Math.min(Math.floor(rem / 365), 3);
  const copticYear = fullCycles * 4 + extraYears + 1;
  const dayOfYear = rem - extraYears * 365;
  const copticMonth = Math.floor(dayOfYear / 30) + 1;
  const copticDay = (dayOfYear % 30) + 1;

  return { year: copticYear, month: Math.min(copticMonth, 13), day: Math.min(copticDay, 30) };
}

export function getTodayCopticDate() {
  return gregorianToCoptic(new Date());
}

